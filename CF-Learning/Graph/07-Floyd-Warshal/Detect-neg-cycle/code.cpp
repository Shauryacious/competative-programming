class Solution {
  public:
    void floydWarshall(vector<vector<int>> &dist) {
        int n = dist.size();

        for(int k = 0; k < n; k++) {
            for(int i = 0; i < n; i++) {
                for(int j = 0; j < n; j++) {
                    if(dist[i][k] != INF && dist[k][j] != INF) {
                        dist[i][j] = min(dist[i][j], dist[i][k] + dist[k][j]);
                    }
                }
            }
        }

        //Detect negative cycles
        for(int i=0; i < n; i++){
            if(dist[i][i] < 0) {
                // Negative cycle detected
            }
        }
    }
};
