class Solution {
public:
    vector<string> letterCombinations(string s) {
        unordered_map<char, string> mp = 
        {
            {'2', "abc"},
            {'3', "def"},
            {'4', "ghi"},
            {'5', "jkl"},
            {'6', "mno"},
            {'7', "pqrs"},
            {'8', "tuv"},
            {'9', "wxyz"}
        };

        vector<string> ans;
        if (s.empty()) return ans;

        auto f = [&](int i, string curr, auto && f) -> void {
            if (i == s.size()) {
                ans.pb(curr);
                return;
            }

            for(auto ch : mp[s[i]]) {
                curr.pb(ch);
                f(i + 1, curr, f);
                curr.pop_back();
            }
        };

        f(0, "", f);
        return ans;
    }
};